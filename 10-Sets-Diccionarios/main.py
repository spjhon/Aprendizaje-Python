# Un set es un tipo de dato que no tiene indice y no tiene orden

personas = {
    "Victor",
    "Manolo",
    "Francisco"
}

print (type (personas))

# se pueden utilizar todo tipo de metodos, entre ellos es el ad y el remove

personas.add ("Paco")
personas.remove ("Francisco")

print (personas)

# Es mejor una lista que un set
